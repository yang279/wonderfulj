const icons = [];
const usedIds = new Set();

for (const [catName, cat] of Object.entries(categories)) {
  for (let i = 0; i < cat.names.length; i++) {
    const name = cat.names[i];
    const id = name;
    if (usedIds.has(id)) continue;
    usedIds.add(id);
    const description = cat.desc[Math.floor(Math.random() * cat.desc.length)];
    const svg = svgTemplates[Math.floor(Math.random() * svgTemplates.length)];
    icons.push({ id, name, description, svg });
  }
}

